# На вход программе подаётся строка текста, содержащая 4 целых неотрицательных числа, разделённых точкой.
# Напишите программу, которая определяет, является ли введённая строка текста корректным ip-адресом.
# Программа должна вывести «ДА» (без кавычек), если введённая строка является корректным ip-адресом,
# или «НЕТ» (без кавычек) в противном случае. ip-адрес является корректным, если все 4 числа находятся
# в диапазоне от 0 до 255 включительно.
s = input()
lict = s.split('.')  # разбиваем строку по точкам
if len(lict) != 4:  # проверяем, что ровно 4 части
    print('НЕТ')
else:
    correct = True
    for i in lict:  # перебираем все 4 части строки
        if not i.isdigit():  # проверяем, что часть - число
            correct = False
            break
        num = int(i)  # преобразуем строку в число
        if num < 0 or num > 255:
            correct = False
            break
    if correct:
        print('ДА')
    else:
        print('НЕТ')

